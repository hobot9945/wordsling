package com.example.wordsling

import android.text.Editable
import android.util.Log
import android.view.inputmethod.BaseInputConnection

/**
* Обрабатывает ввод текста с учетом правил мягкой клавиатуры (gboard) и формирует протокол
* wordsling для сервера.
*
* # Протокол gboard
* При вводе текста через мягкую клавиатуру Android выделяет набираемый участок как "композитный"
* (composing text).
* Этот текст может многократно изменяться клавиатурой (автокоррекция, подбор слов, ввод слогов),
* пока не будет зафиксирован. Признак активной композиции — индекс начала композитного спана
* (`spanStartInd >= 0`).
* Текст до этого индекса считается стабильным и не меняется клавиатурой. Когда композиция
* завершается, клавиатура начинает новую, индекс спана меняется. Это означает фиксацию
* (стабилизацию) предыдущего текста.
*
* # Протокол wordsling
* Формирует строку дельты, описывающую изменение текста относительно предыдущей итерации.
* Формат дельты: `[*][N]текст`
* - `*` (стабилизация): ставится в начало, если зафиксировано изменение позиции композитного спана.
*   Сигнализирует серверу, что предыдущий текст стабилизирован и не будет изменяться последующими
* дельтами.
* - `[N]` (забои): указывает количество символов N для удаления с конца текущего текста.
* Опускается, если удаления нет.
* - `текст`: символы для добавления в конец текста после забоев. Опускается, если добавления нет.
* Если изменений нет, дельта пустая.
*
* ОТВЕТСТВЕННОСТЬ:
* - Хранение опорного текста (состояние поля ввода на прошлой итерации).
* - Отслеживание состояния композиции (gboard).
* - Формирование строки дельты по протоколу wordsling.
*/
class TextProcessor {

    // Опорный текст прошлой итерации. Это весь текст поля ввода. Но, при расчете дельты нас будет
    // интересовать только его композитная часть, весь текст до нее считаем стабильным.
    private var _referenceText: String = ""

    // Позиция начала композита на прошлой итерации
    private var _refSpanStartInd = -1

    // Маркеры стабилизации: перед и после дельты
    private var _prefStableMarker = ""
    private var _suffStableMarker = ""

    /** Признак того, что в текущем состоянии текста активна композиция IME. */
    var compositionFlag = false

    /**
     * Последняя вычисленная дельта в формате протокола `wordsling`. Пустая строка означает
     * отсутствие изменений.
     */
    var delta: String = ""

    /**
     * Принимает текущее состояние поля ввода, вычисляет дельту
     * относительно предыдущей итерации и обновляет внутреннее состояние.
     *
     * Метод:
     * - читает позицию начала composing-span;
     * - определяет, произошла ли стабилизация предыдущего композитного участка;
     * - вычисляет дельту только по изменяемой части текста;
     * - обновляет опорный текст и служебные поля процессора.
     *
     * @param s текущее содержимое поля ввода после срабатывания `afterTextChanged()`.
     * @return текущий экземпляр `TextProcessor` после обновления состояния.
     *
     * Побочные эффекты:
     * - изменяет `_referenceText`;
     * - изменяет `_refSpanStartInd`;
     * - обновляет `compositionFlag`;
     * - обновляет `delta`.
     */
    fun takeText(s: Editable): TextProcessor {

        // Индекс начала композитной части. Если она есть, индекс >= 0.
        val spanStartInd = BaseInputConnection.getComposingSpanStart(s)

        // Если есть участок композиции, поднимаем флаг
        compositionFlag = spanStartInd >= 0

        // Индекс начала изменяемой части текста поля ввода.
        // Если идет композиция, изменяется только часть от начала спана.
        // Иначе сравниваем строки целиком (индекс = 0), что корректно отрабатывает и удаления.
        val mutableInd = if (compositionFlag) spanStartInd.coerceIn(0, s.length) else 0
Log.d("TextProcessor", "spanStartInd: $spanStartInd, mutableInd: $mutableInd, s: $s")

        // Новый вариант изменяемой части.
        val newString = s.toString().substring(mutableInd)

        // Опорный вариант изменяемой части.
        // Защита от выхода индекса за пределы старой строки, если спан сместился вправо.
        val refString =
            if (mutableInd > _referenceText.length) "" else _referenceText.substring(mutableInd)

        // Новая дельта. Если прошлый текст стабилизирован, ставим префикс `*` без разделяющих пробелов
        delta = _extractDelta(newString, refString)

        // Установить маркеры стабилизации
        _setStabilizationMarkers(spanStartInd, delta.isEmpty())
        delta = "$_prefStableMarker$delta$_suffStableMarker"

        _refSpanStartInd = spanStartInd

Log.d("TextProcessor", "delta: $delta, newString: $newString, refString: $refString")

        // Обновляем опорный текст для следующей итерации
        _referenceText = s.toString()

        return this
    } // takeText()

    /**
     * Вычисляет дельту между новым и опорным фрагментами изменяемой части текста.
     *
     * Алгоритм:
     * - находит общий префикс `refString` и `newString`;
     * - определяет, сколько символов нужно удалить с конца опорной строки;
     * - определяет, какой хвост нужно дописать.
     *
     * Формат результата: `[N]текст`.
     * Если удаления нет, префикс `[N]` опускается.
     * Если добавления нет, текстовая часть опускается.
     * Если изменений нет совсем, возвращается пустая строка.
     *
     * @param newString текущий вариант изменяемой части текста.
     * @param refString опорный вариант изменяемой части текста.
     * @return строка дельты или пустая строка, если изменений нет.
     */
    private fun _extractDelta(newString: String, refString: String): String {

        // Находим длину совпадающего начала
        val commonPrefixLength = refString.commonPrefixWith(newString).length

        // Определяем, сколько символов было "удалено" с конца старой строки
        val charsToDelete = refString.length - commonPrefixLength

        // Определяем, какой текст был добавлен после общего префикса
        val textToAdd = newString.substring(commonPrefixLength)

        // Формируем строку команды
        return buildString {
            if (charsToDelete > 0) {
                append("[$charsToDelete]")
            } // if
            if (textToAdd.isNotEmpty()) {
                append(textToAdd)
            } // if
        } // buildString
    } // extractDelta()

    /**
     * Вычисляет и устанавливает маркеры стабилизации текста для протокола wordsling.
     *
     * Маркеры сигнализируют серверу о статусе текста:
     * - `_prefStableMarker` ("*"): весь предшествующий текст стабилизирован.
     * - `_suffStableMarker` ("*"): весь текст, включая текущую дельту, стабилизирован.
     *
     * Правила установки (маркеры исключают друг друга):
     * - Спан исчез (curr == -1) и был до этого (prev >= 0):
     *   - Если дельта пустая, ставится префикс (зафиксирован старый текст).
     *   - Если дельта не пустая, ставится суффикс (зафиксирован старый текст + новая дельта).
     * - Спан сместился (curr != prev), но не исчез, и был до этого (prev >= 0):
     *   Ставится префикс (старая композиция зафиксирована, началась новая).
     * - Спана нет и не было (curr == -1, prev == -1), но есть дельта:
     *   Ставится суффикс (вводится стабильный текст вне композиции, например при вставке).
     * - Во всех остальных случаях (в т.ч. активная неизменная композиция) маркеры сбрасываются.
     *
     * @param spanStartInd индекс начала текущего спана (-1, если спана нет).
     * @param isDeltaEmpty признак отсутствия текстовых изменений в текущей итерации.
     */
    private fun _setStabilizationMarkers(spanStartInd: Int, isDeltaEmpty: Boolean) {
        _prefStableMarker = ""
        _suffStableMarker = ""

        val curr = spanStartInd
        val prev = _refSpanStartInd

        if (curr == -1 && prev >= 0) {
            // Спан исчез. Весь текст стабилизирован.
            if (isDeltaEmpty) {
                // Текст не изменился, просто зафиксирован.
                _prefStableMarker = "*"
            } else {
                // Текст изменился и сразу зафиксирован.
                _suffStableMarker = "*"
            } // if
        } else if (curr != prev && prev >= 0) {
            // Спан сместился, но не исчез. Предшествующий текст стабилизирован, новая часть — нет.
            _prefStableMarker = "*"
        } else if (curr == -1 && prev == -1 && !isDeltaEmpty) {
            // Спана нет и не было, но текст вводится. Считаем ввод стабильным.
            _suffStableMarker = "*"
        } // if
    } // _setStabilizationMarkers()

    /**
     * Преобразует строку в нижний регистр и заменяет знаки пунктуации
     * на текстовые эквиваленты согласно карте замен.
     *
     * Обработка посимвольная. После замены множественные пробелы
     * схлопываются в один.
     *
     * Метод пока не используется и оставлен как заготовка для возможной
     * серверной нормализации текста.
     *
     * @param str исходная строка.
     * @return строка в нижнем регистре с замененной пунктуацией
     * и нормализованными пробелами.
     */
    private fun _convertToLowercaseAndDepunctuate(str: String): String {
        // 1. Преобразуем в нижний регистр, мапим символы через карту замен, собираем обратно
        val processedStr = str
            .lowercase()
            .map { char ->
                val charStr = char.toString()
                _punctuationMap[charStr] ?: charStr
            }
            .joinToString("")

        // 2. Убираем множественные пробелы
        return processedStr.replace(_multipleSpacesRegex, " ")
    } // _convertToLowercaseAndDepunctuate()

    private val _multipleSpacesRegex = Regex("\\s+")
    private val _punctuationMap: HashMap<String, String> = hashMapOf(
        "." to " точка",
        "," to " запятая",
        "\n" to " новая строка ",   // есть хвостовой пробел, перевод строки немножко особенный символ
        ";" to " точка с запятой",
        ":" to " двоеточие",
        "?" to " вопросительный знак",
        "!" to " восклицательный знак",
        "#" to " диез ",            // некоторые символы gboard не отделяет пробелом
        "/" to " слэш ",            // некоторые символы gboard не отделяет пробелом
        "'" to " апостроф",         // некоторые символы gboard не отделяет пробелом
        "@" to " собака ",
        "-" to " тире ",            // образует множественные пробелы: "как-то" vs "они - козлы"
    )

    /**
     * Переустанавливает опорное состояние процессора после внешней подчистки поля ввода.
     *
     * После вызова метода новый текст считается базовым состоянием,
     * относительно которого будут вычисляться следующие дельты.
     *
     * @param refStr новый опорный текст.
     */
    fun reset(refStr: String) {
        _referenceText = refStr
        _refSpanStartInd = -1
    } // reset()
} // TextProcessor